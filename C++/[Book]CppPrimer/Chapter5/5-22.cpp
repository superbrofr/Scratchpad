#include <iostream>

using namespace std;

int main()
{
	cout << "Char: " << sizeof(char) << " byte/s" << endl;
	cout << "Int: " << sizeof(int) << " byte/s" << endl;
	cout << "Long: " << sizeof(long) << " byte/s" << endl;
	cout << "Boolean: " << sizeof(bool) << " byte/s" << endl;
	cout << "Float: " << sizeof(float) << " byte/s" << endl;
	return 0;
}